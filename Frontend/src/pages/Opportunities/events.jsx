// Helper functions for working with events data

/**
 * Get event by ID
 * @param {number} id - Event ID
 * @returns {Object|undefined} Event object or undefined if not found
 */
export const getEventById = (id) => {
  return events.find((event) => event.id === id);
};

/**
 * Get events by category
 * @param {string} category - Category name
 * @returns {Array} Array of events matching the category
 */
export const getEventsByCategory = (category) => {
  return events.filter((event) => event.category === category);
};

/**
 * Get all open events with available slots
 * @returns {Array} Array of open events
 */
export const getOpenEvents = () => {
  return events.filter(
    (event) => event.status === "open" && event.availableSlots > 0
  );
};

/**
 * Get all full events (no available slots)
 * @returns {Array} Array of full events
 */
export const getFullEvents = () => {
  return events.filter((event) => event.registered === event.capacity);
};

/**
 * Get all unique categories
 * @returns {Array} Array of category names
 */
export const getAllCategories = () => {
  return [...new Set(events.map((event) => event.category))];
};

/**
 * Get events by date range
 * @param {string|Date} startDate - Start date
 * @param {string|Date} endDate - End date
 * @returns {Array} Array of events within date range
 */
export const getEventsByDateRange = (startDate, endDate) => {
  const start = new Date(startDate);
  const end = new Date(endDate);
  return events.filter((event) => {
    const eventDate = new Date(event.date);
    return eventDate >= start && eventDate <= end;
  });
};

/**
 * Get events by location (partial match)
 * @param {string} location - Location to search for
 * @returns {Array} Array of events matching location
 */
export const getEventsByLocation = (location) => {
  return events.filter((event) =>
    event.location.toLowerCase().includes(location.toLowerCase())
  );
};

/**
 * Get events sorted by date
 * @param {boolean} ascending - Sort ascending if true, descending if false
 * @returns {Array} Sorted array of events
 */
export const getEventsSortedByDate = (ascending = true) => {
  return [...events].sort((a, b) => {
    const dateA = new Date(a.date);
    const dateB = new Date(b.date);
    return ascending ? dateA - dateB : dateB - dateA;
  });
};

/**
 * Get events with low availability (less than 10% slots available)
 * @returns {Array} Array of events with low availability
 */
export const getLowAvailabilityEvents = () => {
  return events.filter((event) => {
    const availabilityPercent = (event.availableSlots / event.capacity) * 100;
    return availabilityPercent < 10 && availabilityPercent > 0;
  });
};

// Export default for convenience
export default events;
